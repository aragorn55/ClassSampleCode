#include "Math.h"
//add two numbers together
double Math::Add(double first_value, double  second_double)
{
	double total = 0;
	total = first_value + second_double;
	return total;

}
//subtract one number from the first
double Math::Subtract(double first_value, double  second_double)
{
double total = 0;
total = first_value - second_double;
	return total;


}
//multiply two numbers together
double Math::Multiply(double first_value, double  second_double)
{
double total = 0;
total = first_value * second_double;
	return total;


}
// Divide the first number by the second number
double Math::Divide(double first_value, double  second_double)
{
double total = 0;
total = first_value / second_double;
	return total;

}
//find the average of two numbers
double Math::FindAverage(double first_value, double  second_double)
{
double total = 0;
total = (first_value + second_double) / 2;
	return total;

}